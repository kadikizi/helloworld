package Cydeopage25;

public class Foreach {
    public static void main(String[] args) {
        //foreach döngüsü, bir koleksiyonun veya dizinin tüm elemanları üzerinde dolaşmak için kullanılır.
        // Her döngü turunda, koleksiyonun veya dizinin bir elemanı alınır ve belirtilen işlemler bu eleman üzerinde gerçekleştirilir.
        // foreach döngüsü,daha kolay okunabilir ve hata yapma olasılığını azaltır.
        int[] sayilar = {1, 2, 3, 4, 5};

        for (int sayi : sayilar) {
            System.out.println(sayi);
        }
    }
    }
