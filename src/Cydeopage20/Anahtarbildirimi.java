package Cydeopage20;

public class Anahtarbildirimi {
    public static void main(String[] args) {
        //Java'da anahtar bildirimi (keyword declaration),
        // değişken veya fonksiyon isimlerinin belirli anahtar kelimelerle çakışmasını önlemek için kullanılan bir yaklaşımdır.
        // Anahtar bildirimleri, Java programlama dilinde tanımlanan önceden belirlenmiş anahtar kelimelerdir ve
        // bu kelimeler programın sözdiziminde belirli amaçlar için ayrılmıştır.
        //Anahtar bildirimleri, bir değişken veya fonksiyon adı olarak kullanılamazlar.
        // Bu sayede, programın okunabilirliği artar ve dilin sözdizimindeki kavramlarla çakışma sorunları önlenebilir.
        //Örneğin, "int" bir anahtar kelime (keyword) olup, tam sayı türünde bir değişken tanımlamak için kullanılır.
        // Eğer "int" adında bir değişken tanımlanmaya çalışırsa, hata alınır çünkü anahtar
        // bir kelimeyi değişken adı olarak kullanmak geçerli değildir.

        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }


    }
}
