package Diziler2;

public class Touppercase {
    public static void main(String[] args) {

        //toUpperCase yöntemi, bir String'in tüm karakterlerini büyük harfe dönüştürmek için kullanılır.
        // Bu yöntem, orijinal String'i değiştirmez, büyük harf dönüşümü yapılmış yeni bir String nesnesi döndürür.

        String str = "merhaba dünya";
        String buyukHarfli = str.toUpperCase();
        System.out.println(buyukHarfli);



    }
}
