package Diziler2;

public class Charat {
    public static void main(String[] args) {
        //charAt yöntemi, bir String'in belirli bir indisindeki karakteri almak için kullanılır.
        // Bu yöntem, verilen indis numarasına karşılık gelen karakteri döndürür.

        String str = "Merhaba";
        char karakter = str.charAt(2);
        System.out.println(karakter);

    }
}
