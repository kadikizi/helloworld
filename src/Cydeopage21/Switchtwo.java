package Cydeopage21;

public class Switchtwo {

    public static void main(String[] args) {

        int number = 5;

        switch (number) {
            case 1:
                System.out.println("Sayı 1'e eşittir.");
                break;
            case 2:
                System.out.println("Sayı 2'ye eşittir.");
                break;
            case 3:
                System.out.println("Sayı 3'e eşittir.");
                break;
            case 4:
                System.out.println("Sayı 4'e eşittir.");
                break;
            case 5:
                System.out.println("Sayı 5'e eşittir.");
                break;
            default:
                System.out.println("Sayı başka bir değere eşittir.");
                break;
        }

    }
}
